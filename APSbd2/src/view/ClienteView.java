    public void listarJTable() {
        DefaultTableModel modelo = (DefaultTableModel) jTClientes.getModel();

        modelo.setRowCount(0);
        ClienteDAO dao = new ClienteDAO();

        for (Cliente c : dao.findAll()) {

            modelo.addRow(new Object[]{
                c.getCodigo(),
                c.getNome(),
                c.getCpf(),
                c.getRg()
            });
        }
    }
    
