    private void jBAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAtualizarActionPerformed
        // TODO add your handling code here:

        if (jTClientes.getSelectedRow() != -1) {
            
            Cliente cli = new Cliente();
            ClienteDAO dao = new ClienteDAO();

            cli.setCodigo((int)jTClientes.getValueAt(jTClientes.getSelectedRow(), 0));
            cli.setNome(jTxtNome.getText());
            cli.setCpf(jTxtCpf.getText());
            cli.setRg(jTxtRg.getText());

            dao.update(cli);
            
            listarJTable();

        }

    }//GEN-LAST:event_jBAtualizarActionPerformed

    private void jTClientesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTClientesMouseClicked
        // TODO add your handling code here:

        if (jTClientes.getSelectedRow() != -1) {

            jTxtNome.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(), 1).toString());
            jTxtCpf.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(), 2).toString());
            jTxtRg.setText(jTClientes.getValueAt(jTClientes.getSelectedRow(), 3).toString());
        }
    }//GEN-LAST:event_jTClientesMouseClicked

    private void jBExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBExcluirActionPerformed
        // TODO add your handling code here:
        
        if (jTClientes.getSelectedRow() != -1) {
            
            Cliente cli = new Cliente();
            ClienteDAO dao = new ClienteDAO();

            cli.setCodigo((int)jTClientes.getValueAt(jTClientes.getSelectedRow(), 0));            

            dao.delete(cli);
            
            listarJTable();

        }
    }//GEN-LAST:event_jBExcluirActionPerformed

